package com.gauri.demo;

public class Employee {
	int employeeId;
	String name;
	String address;
	double phoneNumber;

	Employee(){
		
	}

	Employee(int employeeId, String name, String address, double phoneNumber) {
		this.employeeId = employeeId;
		this.name = name;
		this.address = address;
		this.phoneNumber = phoneNumber;

	}

}
